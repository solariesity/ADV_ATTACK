# 生成「红色 + 白色沾污渍」颜色集
# ==================================================
def generate_stained_colors(
    n_white=100,
    n_red=80,
    device=config.device0,
):
    """
    分别生成白色和红色的污渍颜色集
    返回：
        white_colors: [n_white, 3] 白色污渍颜色（RGB, 值范围[0,1]）
        red_colors: [n_red, 3]     红色污渍颜色（RGB, 值范围[0,1]）
    """
    # ==========================
    # 白色污渍（低饱和度 + 高明度）
    # ==========================
    h_white = torch.zeros(n_white, device=device)
    s_white = torch.zeros(n_white, device=device)  # 饱和度 ≤0.05
    v_white = 0.75 + torch.rand(n_white, device=device) * 0.25  # 明度 ∈[0.75,1.0]
    white_colors = hsv_to_rgb(h_white, s_white, v_white)  # [n_white, 3]

    # ==========================
    # 红色污渍（严格红色调 + 高饱和度 + 低明度）
    # ==========================
    h_red = (torch.rand(n_red, device=device) * 2.0 - 1.0) / 360.0  # 色相 ±1°
    h_red = h_red % 1.0
    s_red = 0.6 + torch.rand(n_red, device=device) * 0.3  # 饱和度 ∈[0.6,0.9]
    v_red = (0.4 + torch.rand(n_red, device=device) * 0.35) ** 1.3  # 明度非线性压暗
    red_colors = hsv_to_rgb(h_red, s_red, v_red)  # [n_red, 3]

    return white_colors.detach(), red_colors.detach()


def generate_yellow_black_stains(
    n_yellow=90,
    n_black=100,
    device=config.device0,
):
    """
    生成铁锈黄色和黑色脏污颜色集

    参数:
        n_yellow: 黄色脏污颜色数量
        n_black: 黑色脏污颜色数量
        device: 计算设备

    返回:
        yellow_colors: [n_yellow, 3] 铁锈黄色脏污颜色（RGB, 值范围[0,1]）
        black_colors: [n_black, 3]   黑色脏污颜色（RGB, 值范围[0,1]）
    """
    # ==========================
    # 铁锈黄色脏污
    # 特点：偏暗、偏橙黄、饱和度中等偏高
    # ==========================
    # 色相范围：50°~70°（归一化后0.139~0.194），覆盖铁锈黄到土黄色
    h_yellow = 0.14 + torch.rand(n_yellow, device=device) * 0.02

    # 饱和度：0.55~0.85，保持较高的饱和度体现锈迹
    s_yellow = 0.75 + torch.rand(n_yellow, device=device) * 0.3

    # 明度：0.60~0.85，偏暗的黄色体现陈旧感
    v_yellow = 0.6 + torch.rand(n_yellow, device=device) * 0.25

    # 转换到RGB
    yellow_colors = hsv_to_rgb(h_yellow, s_yellow, v_yellow)  # [n_yellow, 3]

    # ==========================
    # 黑色脏污
    # 特点：低明度、低饱和度（略带色调）
    # ==========================
    # 色相：略微偏向暖色调（0.0~0.1，偏红黄），体现脏污感而非纯黑
    h_black = torch.rand(n_black, device=device) * 0.1

    # 饱和度：0.1~0.4，略带颜色变化
    s_black = 0.1 + torch.rand(n_black, device=device) * 0.3

    # 明度：0.25~0.45，非常暗但不完全黑
    v_black = 0.25 + torch.rand(n_black, device=device) * 0.2

    # 转换到RGB
    black_colors = hsv_to_rgb(h_black, s_black, v_black)  # [n_black, 3]

    return yellow_colors.detach(), black_colors.detach()


