# ==========================================
# Loss：像素到「污渍红/白」颜色集的最小距离
# ==========================================
def stain_color_loss(
    tensor: torch.Tensor,
    target: bool = int,  #  "red":0 "white":1 "yellow":2 "black":3
    mask: torch.Tensor = None,  # 新增：掩码 [B, H, W] 或 [1, H, W]，值域[0,1]
    color_power: int = 1,
):
    """
    计算像素到目标颜色集（红/白）的最小距离损失（支持掩码区域计算）
    参数：
        tensor: [B, 3, H, W] 输入图像（RGB, 值范围[0,1]）
        target: "white" 或 "red"，指定计算白色或红色的距离
        mask: 可选，[B, H, W] 或 [1, H, W]，仅计算mask=1的区域损失
        color_power: 1=均值, 2=平方（压制颜色峰值）
    返回：
        loss: 掩码区域内的平均最小距离
    """
    assert white_colors is not None and red_colors is not None, "颜色集未提供"

    # 选择目标颜色集
    if target == 0:
        stain_colors = red_colors
    elif target == 1:
        stain_colors = white_colors
    elif target == 2:
        stain_colors = yellow_colors
    elif target == 3:
        stain_colors = black_colors

    # 计算所有像素的最小距离
    tensor = tensor.permute(0, 2, 3, 1)  # [B, H, W, 3]
    distances = torch.norm(tensor.unsqueeze(-2) - stain_colors, dim=-1)  # [B, H, W, K]
    min_distances = torch.min(distances, dim=-1)[0]  # [B, H, W]

    if color_power != 1:
        min_distances = min_distances ** color_power

    # 应用掩码（如果提供）
    if mask is not None:
        # 确保mask与tensor形状兼容（允许mask为[B, H, W]或[1, H, W]）
        if mask.dim() == 3 and mask.size(0) == 1:
            mask = mask.expand_as(min_distances)  # [1, H, W] -> [B, H, W]（广播）
        elif mask.dim() == 3 and mask.size(0) != min_distances.size(0):
            raise ValueError(f"mask的batch_size不匹配：{mask.shape} vs {min_distances.shape}")

        # 仅计算mask=1的区域
        min_distances = min_distances * mask  # 掩码外区域归零
        valid_pixels = mask.sum() + 1e-6  # 避免除零
        loss = min_distances.sum() / valid_pixels
    else:
        loss = torch.mean(min_distances)

    return loss


def stain_color_loss_patch(tensor, scene_obj_mask):
    """
    tensor: [B, 3, H, W], value in [0,1]
    scene_obj_mask: [B, 3 or 1, H, W]   car 在 scene 中的位置
    stain_colors: [K, 3]                (全局变量)
    """

    # ---- 1. 统一 mask 维度 ----
    if scene_obj_mask.dim() == 4:
        mask = scene_obj_mask[:, 0, :, :]  # [B, H, W]
    else:
        mask = scene_obj_mask  # [B, H, W]

    # ---- 2. 原始颜色 loss 计算 ----
    # [B, H, W, 3]
    tensor = tensor.permute(0, 2, 3, 1)

    # [B, H, W, K]
    distances = torch.norm(tensor.unsqueeze(-2) - stain_colors, dim=-1)

    # [B, H, W]
    min_distances = torch.min(distances, dim=-1)[0]

    # ---- 3. 只在 car 区域计算 loss ----
    masked_loss = min_distances * mask

    loss = masked_loss.sum() / (mask.sum() + 1e-6)

    return loss


def original_color_loss_2(tensor1, tensor2):
    """
    计算两个图像张量之间的欧氏距离损失。

    Args:
        tensor1: [B, 3, H, W], value in [0,1]
        tensor2: [B, 3, H, W], value in [0,1]

    Returns:
        loss: scalar, 平均欧氏距离
    """
    # 确保两个 tensor 形状一致
    assert tensor1.shape == tensor2.shape, "Input tensors must have the same shape"

    # 计算两个 tensor 的差值
    diff = tensor1 - tensor2

    # 沿着通道维度 (dim=1) 计算 L2 范数 (欧氏距离)
    # 结果形状为 [B, H, W]
    distances = torch.norm(diff, dim=1)

    # 计算所有空间位置的平均距离作为 loss
    return torch.mean(distances)


def original_color_loss(tensor1, tensor2, mask=None):
    """
    计算两个图像 tensor 的颜色差（稳定、有梯度）

    Args:
        tensor1: [B, 3, H, W]  网络输出
        tensor2: [B, 3, H, W]  参考图像
        mask:    [B, 1 or 3, H, W] 或 [B, H, W]，可选

    Returns:
        loss: scalar
    """
    assert tensor1.shape == tensor2.shape, "Input tensors must have same shape"

    # ---- 1. 统一 mask ----
    if mask is not None:
        if mask.dim() == 4:
            mask = mask[:, 0, :, :]  # [B, H, W]
    else:
        mask = torch.ones(tensor1.shape[0], tensor1.shape[2], tensor1.shape[3], device=tensor1.device)

    # ---- 2. 颜色差（不取根号，梯度最稳）----
    # [B, 3, H, W] → [B, H, W]
    diff = tensor1 - tensor2
    color_diff = torch.sum(diff**2, dim=1)

    # ---- 3. mask 区域平均 ----
    masked_loss = color_diff * mask
    loss = masked_loss.sum() / (mask.sum() + 1e-6)

    return loss


