def generate_cmyk_printable_colors(steps=6, device="cuda:0"):
    c = torch.linspace(0, 1, steps, device=device)
    m = torch.linspace(0, 1, steps, device=device)
    y = torch.linspace(0, 1, steps, device=device)
    k = torch.tensor([0.0, 0.5], device=device)

    colors = []
    for ci in c:
        for mi in m:
            for yi in y:
                for ki in k:
                    # 避免极端墨水组合
                    if (ci + mi + yi + ki) <= 1.5:
                        # 简化 CMYK -> RGB
                        r = (1 - ci) * (1 - ki)
                        g = (1 - mi) * (1 - ki)
                        b = (1 - yi) * (1 - ki)

                        colors.append(torch.stack([r, g, b]))

    return torch.stack(colors)


