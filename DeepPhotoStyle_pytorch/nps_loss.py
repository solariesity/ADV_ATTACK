def nps_loss(tensor):
    # 调整张量形状为 [B, H, W, 3] 以便计算
    tensor = tensor.permute(0, 2, 3, 1)  # [B, H, W, 3]

    # 计算每个像素与所有可打印颜色的最小欧氏距离
    distances = torch.norm(tensor.unsqueeze(-2) - printable_colors, dim=-1)  # [B, H, W, K]
    min_distances = torch.min(distances, dim=-1)[0]  # [B, H, W]
    return torch.mean(min_distances)  # 平均最小距离作为NPS Loss


def nps_loss_patch(tensor, scene_obj_mask):
    """
    tensor: [B, 3, H, W], value in [0,1]
    scene_obj_mask: [B, 3 or 1, H, W]   car 在 scene 中的位置
    printable_colors: [K, 3]            (全局变量)
    """

    # ---- 1. 统一 mask 维度 ----
    if scene_obj_mask.dim() == 4:
        mask = scene_obj_mask[:, 0, :, :]  # [B, H, W]
    else:
        mask = scene_obj_mask  # [B, H, W]

    # ---- 2. 原始 NPS loss 计算 ----
    # [B, H, W, 3]
    tensor = tensor.permute(0, 2, 3, 1)

    # [B, H, W, K]
    distances = torch.norm(tensor.unsqueeze(-2) - printable_colors, dim=-1)

    # [B, H, W]
    min_distances = torch.min(distances, dim=-1)[0]

    # ---- 3. 只在 car 区域计算 loss ----
    masked_loss = min_distances * mask

    loss = masked_loss.sum() / (mask.sum() + 1e-6)

    return loss


