def save_color_grid(color_tensor, output_path="color_grid.png", block_size=50, cols=10):
    """
    将颜色张量保存为2x2颜色块的网格图片
    参数：
        color_tensor: [N, 3] RGB颜色张量（值范围0-1）
        output_path: 输出图片路径
        block_size: 每个颜色块的大小（像素）
        cols: 每行显示的颜色数量
    """
    # 转换为numpy数组并确保值范围正确
    colors = color_tensor.cpu().numpy()
    colors = np.clip(colors, 0, 1) * 255  # 转换为0-255范围

    # 计算行数和画布尺寸
    rows = int(np.ceil(len(colors) / cols))
    canvas_width = cols * block_size
    canvas_height = rows * block_size

    # 创建空白RGB画布
    canvas = Image.new("RGB", (canvas_width, canvas_height), (255, 255, 255))

    # 绘制颜色块
    for i, color in enumerate(colors):
        row = i // cols
        col = i % cols
        x_start = col * block_size
        y_start = row * block_size

        # 创建单个颜色块
        block = Image.new("RGB", (block_size, block_size), tuple(color.astype(int)))
        canvas.paste(block, (x_start, y_start))

    # 保存图片前确保输出目录存在
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    canvas.save(output_path)
    print(f"颜色网格已保存至: {output_path}")


